#include <stdio.h>

int main(){

	int a = 10, b = 5;
	int *p;
	p = &a;
	
	printf("a = %d, p = %p, *p = %d\n", a,p,*p);
	
	*p = 4;
	printf("a = %d, p = %p, *p = %d\n", a,p,*p);
	
	p = &b;
	printf("a = %d, p = %p, *p = %d\n", a,p,*p);
	
	*p = 11;
	printf("a = %d, p = %p, *p = %d\n", a,p,*p);
	
	return(0);

}
